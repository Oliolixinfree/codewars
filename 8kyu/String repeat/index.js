function repeatStr(n, s) {
	return s.repeat(n)
}

console.log(repeatStr(3, '*'))
console.log(repeatStr(5, '#'))
console.log(repeatStr(2, 'ha '))
console.log(repeatStr(0, ''))
console.log(repeatStr(0, 'I'))
console.log(repeatStr(5, ''))
console.log(repeatStr(6, 'I'))
console.log(repeatStr(5, 'Hello'))
